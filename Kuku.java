class Kuku {
    public static void main(Strgin[] args) {
        for (int i = 1; i < 10; i++) {
            for (int j = 1; j < 10; j++) {

                // 掛け算の結果を出力
                System.out.print(i * j);
                
                // 空白を出力
                System.out.print(" ");
            }

            // 改行を出力
            System.out.println("");
        }
    }
}